@st.cache_data
def train_prophet_model(data):
    model = Prophet(
        daily_seasonality=False,
        weekly_seasonality=True,
        yearly_seasonality=True,
        interval_width=0.95  # 95% confidence interval
    )
    model.fit(data)
    return model

model = train_prophet_model(prophet_df)

