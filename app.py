    x=df['week_num'],
    y=df['cases'],
    mode='lines+markers',
    name='‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡∏à‡∏£‡∏¥‡∏á',
    line=dict(color='blue'),
