# ‡πÑ‡∏°‡πà‡∏ï‡πâ‡∏≠‡∏á import matplotlib.pyplot ‡πÅ‡∏•‡∏∞ matplotlib.font_manager ‡πÅ‡∏•‡πâ‡∏ß
# ‡πÑ‡∏°‡πà‡∏ï‡πâ‡∏≠‡∏á‡∏°‡∏µ‡πÇ‡∏Ñ‡πâ‡∏î plt.rcParams[...] ‡∏´‡∏£‡∏∑‡∏≠ fm._rebuild() ‡πÅ‡∏•‡πâ‡∏ß (‡∏ô‡∏µ‡πà‡∏Ñ‡∏∑‡∏≠‡∏Ç‡πâ‡∏≠‡∏î‡∏µ!)

